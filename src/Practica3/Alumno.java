package Practica3;

public class Alumno {
    private String nombre;
    private String apellido;
    private String lu;
    private char sexo;
    private String condicion;
    private int notaFinal;

    public Alumno (String nombre, String apellido, String lu, char sexo, String condicion, int notaFinal) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.lu = lu;
        this.sexo = sexo;
        this.condicion = condicion;
        this.notaFinal = notaFinal;
    }
}
